    private boolean validaCampos() {
        if (txtNomeCliente.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Selecione um Cliente");
            bntBuscarCliente.doClick();
            return false;
        }

        if (this.itens.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Venda sem itens");
            return false;
        }

        return true;
    }

    private void limpaCampos() {
        txtContatoCliente.setText("");
        txtNomeCliente.setText("");
        txtDescontoPorcentagem.setText("");
        txtDescontoValor.setText("");
        txtValorTotalItens.setText("");
        txtTotal.setText("");
        txtTotalItens.setText("");
        jComboFormaPagamento.setSelectedIndex(0);

        preecheDataHora();

        DefaultTableModel dtm = (DefaultTableModel) tabelaVendas.getModel();
        dtm.setRowCount(0);

        this.itens = null;
        this.orcamento = null;
    }

    private void baixaEstoque(Produto produto, int quantidade) {
        EstoqueDAO dao = new EstoqueDAO(TirsoBikes.getEntityManager());
        List<Estoque> estoques = dao.ultimoEstoque(produto.getIdproduto());
        Estoque estoque = estoques.get(0);
        estoque.setQuantidade(estoque.getQuantidade() - quantidade);
        estoque.setData(this.orcamento.getDataHora());
        estoque.setIdvenda(orcamento);
        dao.salvarEstoque(estoque);
    }
