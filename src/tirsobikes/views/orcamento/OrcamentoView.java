    private void bntSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntSalvarActionPerformed
        if (!validaCampos()) {
            return;
        }

        this.orcamento.setDataHora(FormatarData.juntaDataHota(txtDataVenda.getText(), txtHoraVenda.getText()));
        this.orcamento.setDataPagamento(FormatarData.forDB(txtDataPagamento.getText()));
        if (!txtDescontoValor.getText().isEmpty()) {
            this.orcamento.setDesconto(Double.parseDouble(txtDescontoValor.getText().replace(".", "").replace(",", ".")));
        }
        this.orcamento.setFormaPagamento(jComboFormaPagamento.getSelectedItem().toString());
        this.orcamento.setValorFinal(Double.parseDouble(txtTotal.getText().replace(".", "").replace(",", ".")));
        this.orcamento.setInformacoes(txtContatoCliente.getText());

        OrcamentoDAO dao = new OrcamentoDAO(TirsoBikes.getEntityManager());

        this.orcamento = dao.salvarOrcamento(orcamento);



        ItensVendaDAO daoItem = new ItensVendaDAO(TirsoBikes.getEntityManager());

        for (Itensvenda item : this.itens) {
            item.setIditensVenda(null);
            item.setIdvenda(orcamento);
            daoItem.salvaritensVenda(item);
            if (item.getTipo().equals("P")) {
                baixaEstoque(item.getIdproduto(), item.getQuantidade());
            }
        }
        limpaCampos();

    }//GEN-LAST:event_bntSalvarActionPerformed

    private void bntExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntExcluirActionPerformed

        int linhaTabela = tabelaVendas.getSelectedRow();
        if (linhaTabela > -1) {
            int codigo = Integer.parseInt(tabelaVendas.getValueAt(linhaTabela, 0).toString());
            Itensvenda itemAux = new Itensvenda();
            for (Itensvenda item : this.itens) {
                if (item.getIditensVenda() == codigo) {
                    itemAux = item;
                }
            }

            itens.remove(itemAux);
            atualizaTabela();
        }
    }//GEN-LAST:event_bntExcluirActionPerformed
